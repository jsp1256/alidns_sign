#include "header.h"
#define BURSIZE 2048
int hex2dec(char c)
{
    if ('0' <= c && c <= '9')
    {
        return c - '0';
    }
    else if ('a' <= c && c <= 'f')
    {
        return c - 'a' + 10;
    }
    else if ('A' <= c && c <= 'F')
    {
        return c - 'A' + 10;
    }
    else
    {
        return -1;
    }
}

char dec2hex(short int c)
{
    if (0 <= c && c <= 9)
    {
        return c + '0';
    }
    else if (10 <= c && c <= 15)
    {
        return c + 'A' - 10;
    }
    else
    {
        return -1;
    }
}


/**
URL编码函数；
flag为0时候执行标准的URL编码构造；
flag为1时候执行额外的URL编码构造，对‘=’不编码；
*/
void urlencode(char url[],int flag)
{
    int i = 0;
    int len = strlen(url);
    int res_len = 0;
    char res[BURSIZE];
    for (i = 0; i < len; ++i)
    {
        char c = url[i];
        if (    ('0' <= c && c <= '9') ||
                ('a' <= c && c <= 'z') ||
                ('A' <= c && c <= 'Z') ||
                c == '-' || c == '_' ||
                c == '.' || c == '~')
        {
            res[res_len++] = c;
        }/*
        else if(c==':'){
            res[res_len++] = '%';
            res[res_len++] = '2';
            res[res_len++] = '5';
            res[res_len++] = '3';
            res[res_len++] = 'A';
        }*/
        else if (c == '='&&flag)
            res[res_len++] = c;
        else
        {
            int j = (short int) c;
            if (j < 0)
                j += 256;
            int i1, i0;
            i1 = j / 16;
            i0 = j - i1 * 16;
            res[res_len++] = '%';
            res[res_len++] = dec2hex(i1);
            res[res_len++] = dec2hex(i0);
        }
    }
    res[res_len] = '\0';
    strcpy(url, res);
}

